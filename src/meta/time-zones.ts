export function toTimeZones(value: unknown): TimeZones | undefined {
	for (const [timeZone, timeZoneValue] of Object.entries(TimeZones))
		if (
			timeZoneValue === value ||
			timeZone === value ||
			timeZoneValue.toLowerCase() === value ||
			timeZone.toLowerCase() === value
		)
			return timeZoneValue;

	return undefined;
}

